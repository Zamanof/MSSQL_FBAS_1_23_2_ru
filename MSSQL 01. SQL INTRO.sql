База данных — это набор данных, которые обычно связаны и непротиворечивы.

Система управления базами данных - СУБД (Database management system - DBMS) 
 программное обеспечение, хранение данных и управление данными.

Основные функции СУБД:
+ управление данными во внешней памяти (дисках);
+ управление данными, находящимися в оперативной памяти, с помощью дискового кеша;
+ логирование изменений, резервное копирование и восстановление базы после повреждения;
+ Поддержка языков ДБ (язык определения данных, язык манипулирования данными).

Обычно современная СУБД состоит из следующих компонентов:
+ ядро, отвечающее за управление и протоколирование данных во внешней и оперативной памяти,
+ процессор базы данных, который оптимизирует запросы для извлечения 
	и изменения данных и генерирует внутренний код, который обычно не зависит от машины.
+ подсистема поддержки времени выполнения, которая интерпретирует 
	программы манипулирования данными, создающие пользовательский Интерфейс с СУБД
+ также сервисные программы (внешние утилиты), 
	предоставляющие ряд дополнительных возможностей информационной системы по оказанию услуг.

