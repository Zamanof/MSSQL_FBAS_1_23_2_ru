Replication – структура репликации (ответа)
Managment - инструменты администрирования сервера sql


-----------------------------------------------------------------------------------------------
SQL — декларативный язык запросов. Он используется для создания, 
изменения и управления данными, расположенными в соответствующих БД, 
для создания и изменения архитектуры БД, а также для безопасного управления ими.

Запрос (Query) — это команда, написанная на языке SQL.

T-SQL — это диалект языка структурированных запросов, реализованный в MS SQL Server СУБД.

В зависимости от проблемы, которую реализует команда T-SQL, 
она может быть одного из следующих типов:
+ DDL (Data Definition Language). Этот тип включает в себя различные команды, 
	которые управляют базой данных, схемами, индексами, хранимыми процедурами и т. д. 
	Короче говоря, определяет данные. (CREATE, ALTER, DROP, TRUNCATE)
+ DML (Data Manipulation Language). Выбор данных, их обновление, 
	добавление, удаление — короче, все те команды,с помощью которых мы можем 
	управлять данными, относятся к этому типу. (SELECT, UPDATE, INSERT, DELETE)
+ DCL (Data Control Language). К этому типу относятся команды, 
управляющие правами доступа к данным. (GRANT, REVOKE)

