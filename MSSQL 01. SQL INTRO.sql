Реляционная БД

Реляционная модель предполагает хранение данных в схемах, 
которые могут быть связаны друг с другом.

Схема представляет собой набор столбцов и строк.
Столбцы описывают характеристики данных, которые находятся в схеме.
Строки описывают семантический набор свойств (т. е. некоторый объект).

Первичный ключ — это уникальный идентификатор строки в схеме. 
Первичный ключ можно интерпретировать с одним или несколькими значениями (столбцами).
Первичный ключ должен быть уникальным.

