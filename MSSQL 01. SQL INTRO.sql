+ Distributed БД (Распределенная БД) — части MB могут располагаться на двух 
	и более компьютерах

О способе ввода БД:
+ Файл-сервер (File-server) – файлы данных расположены централизованно 
	на файл-сервере, СУБД находится на каждом клиентском компьютере
+ Клиент-сервер (Клиент-сервер) – СУБД размещается на сервере вместе с ДБ 
	и осуществляет доступ к ДБ напрямую в монопольном режиме
+ Встроенная (Embedded) — СУБД, которую можно установить как часть продукта, 
	не требуя процедуры отдельной установки

